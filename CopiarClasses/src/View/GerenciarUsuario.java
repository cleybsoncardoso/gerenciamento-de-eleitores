    private void atualizar(){
        ArrayList<Usuario> users = Controller.getInstance().getUsuarios();
        String[] aux = new String[users.size()];
        for (int i = 0; i < users.size(); i++) {
            aux[i] = users.get(i).getLogin();
        }
        jList2.setListData(aux);
    }
