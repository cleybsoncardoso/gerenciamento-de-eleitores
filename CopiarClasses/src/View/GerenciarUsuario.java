    private void jTextField1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyReleased
        // TODO add your handling code here:
        ArrayList<Usuario> users = Controller.getInstance().getUsuarios();
        ArrayList<Usuario> auxSearch = new ArrayList<Usuario>();
        for (Usuario userAtual : users) {
            if (userAtual.getLogin().contains(this.jTextField1.getText())) {
                auxSearch.add(userAtual);
            }
        }

        String[] aux = new String[auxSearch.size()];
        for (int i = 0; i < auxSearch.size(); i++) {
            aux[i] = auxSearch.get(i).getLogin();
        }
        jList2.setListData(aux);

    }//GEN-LAST:event_jTextField1KeyReleased

    private void jList2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jList2MouseClicked
        // TODO add your handling code here:
        int n = JOptionPane.showConfirmDialog(logado,"Deseja deletar "+this.jList2.getSelectedValue() + " ?","Deletar Usuario",JOptionPane.YES_NO_OPTION);
        if(n == 0){
            Usuario remove = null;
            for(Usuario atual:Controller.getInstance().getUsuarios()){
                if(atual.getLogin().equals(this.jList2.getSelectedValue())){
                    remove = atual;
                }
            }
            Controller.getInstance().getUsuarios().remove(remove);
            this.atualizar();
            jTextField1.setText("");
            Arquivo.write(Controller.getInstance().getDiretorio(), logado, Controller.getInstance());
            
        }
    }//GEN-LAST:event_jList2MouseClicked


