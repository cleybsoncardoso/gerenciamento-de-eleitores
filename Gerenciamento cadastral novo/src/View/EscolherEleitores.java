    private void add(ArrayList<EleitoresNovo> aux) {
        for (int i = auxSelecionados.size() - 1; i >= 0; i--) {

            model.removeRow(i);

        }
        for (EleitoresNovo atual : aux) {
            String auxMsg;
            if (atual.isEnviada()) {
                auxMsg = "ENVIADA";
            } else {
                auxMsg = "FALTA ENVIAR";
            }
            model.insertRow(model.getRowCount(), new Object[]{atual.getNome(), atual.getNascimento(), atual.getTelefone(), atual.getCelular1(), atual.getCelular2(), atual.isMarcado()});

        }
        auxSelecionados = aux;
    }

