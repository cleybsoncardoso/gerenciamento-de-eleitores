    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:

        EleitoresNovo[] ordenar = new EleitoresNovo[auxSelecionados.size()];

        for (int i = 0; i < auxSelecionados.size(); i++) {
            ordenar[i] = auxSelecionados.get(i);
        }

        for (int i = 0; i < ordenar.length; i++) {
            for (int j = 0; j < ordenar.length - 1; j++) {
                if (ordenar[j].getDia() > ordenar[j + 1].getDia()) {
                    EleitoresNovo aux = ordenar[j];
                    ordenar[j] = ordenar[j + 1];
                    ordenar[j + 1] = aux;
                }
            }
        }

        auxSelecionados = new ArrayList<EleitoresNovo>();
        for (int i = 0; i < ordenar.length; i++) {
            auxSelecionados.add(ordenar[i]);
        }
        this.add(auxSelecionados);
    }//GEN-LAST:event_jButton3ActionPerformed

